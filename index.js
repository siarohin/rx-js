function createSubscribe(name = '') {
  return {
    next(x) {
      console.log(name, ': ', x);
    },
    error(err) {
      console.log(`Error: ${err}`);
    },
    complete() {
      console.log(name, ': Completed');
    }
  };
}

rxjs.of()
  .pipe(rxjs.operators.defaultIfEmpty('empty stream'))
  .subscribe(createSubscribe('of'));