function createSubscribe(name = '') {
  return {
    next(x) {
      console.log(name, ': ', x);
    },
    error(err) {
      console.log(`Error: ${err}`);
    },
    complete() {
      console.log(name, ': Completed');
    }
  };
}

// from 5 , 15 elements
rxjs.range(5, 15)
  .subscribe(createSubscribe('range'));